                                    <p>第三十二条
                                        用工单位根据掌握被派遣人员个人绩效计划执行情况及日常管理工作，按照绩效考核规定对被派遣人员在工作能力、工作态度、工作业绩等方面进行全面、客观的考核，并按照单位相关规定给予相应的奖惩，并报公司备案。</p>
