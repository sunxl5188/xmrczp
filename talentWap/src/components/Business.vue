                                    <p><b>第五章 请休假制度</b></p>
                                    <p>第二十一条 被派遣人员应自觉遵守并严格执行用工单位的请休假制度。被派遣人员未按公司或用工单位请假制度办理请假手续而未到岗的，一律按旷工处理。</p>
                                    <p>第二十二条 被派遣人员享有休假权，各类假期规定如下:</p>
                                    <p>（一）婚假:在本公司劳动合同履行期间办理结婚登记的被派遣人员，结婚登记后一年内可享有婚假15天，婚假需在结婚登记后一年内使用，逾期视作自动放弃。</p>
                                    <p>
                                        (二)产假、照顾假:符合计划生育政策女职工产假一律为158天(含产前休假)，如用工单位同意延长产假的，凭用工单位书面同意生育津贴之外的产假工资由用工单位承担，产假最长可延长为180天。配偶符合计划生育政策怀孕生产的，男方照顾假为15天。<br>
                                        女职工怀孕流产的，根据医疗保健部门的意见，适当给予休假。怀孕3个月以内流产的，假期为15天。怀孕3个月以上流产的，假期为42天。</p>
                                    <p>(三)丧假:被派遣人员直系亲属(父母、配偶、子女)死亡时，给予1-3天假期。</p>
                                    <p>（四）病假:被派遣人员患病或受伤的，经县(区)级以上医疗单位出具病假证明，并符合用工单位请假条件的，可请病、伤假，门诊患者每次病假一般不超过3天。</p>
                                    <p>
                                        （五)年休假:在本单位连续工作1年以上的，享受带薪年休假(以下简称年休假)，具体天数:累计工作已满1年不满10年的，年休假5天；已满10年不满20年的，年休假10天；已满20年的，年休假15天。具体实施办法，由用工单位根据生产、工作的具体情况，并考虑职工本人意愿，统筹安排。</p>
                                    <p>（六)事假:被派遣人员因私事需请假时，经个人申请，符合公司及用工单位请假流程及条件的，可请事假。</p>
                                    <p>1.事假应至少提前1天向用工单位递交书面申请，遇有紧急情况的，必须提供合理解释；</p>
                                    <p>2.事假不足4小时者计为半天，超过4小时不足8小时，计为1天；</p>
                                    <p>3.一年内事假一般不超过5天。当年有未休年休假的应先休年休假，再请事假</p>
                                    <p>(七)国家法定休假日按照国家规定执行。本规定如与用工单位相关规定不符，且用工单位规定不违反国家法律的，以用工单位规定为准。</p>
