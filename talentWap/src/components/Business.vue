                                    <p>第十九条 因生产(工作)需要，必须加班的，由用工单位按加班审批程序确定加班事宜。</p>
                                    <p>第二十条 被派遣人员出现下列情况之一的按旷工处理:</p>
                                    <p>(一)拒不接受或不完成公司或用工单位安排的工作任务的；</p>
                                    <p>(二)迟到、早退，月累计3次按旷工1天处理；</p>
                                    <p>(三)未请假或请假未经批准而擅自离开工作岗位的；</p>
                                    <p>(四)未按规定办理请假手续或请假手续不完备的；</p>
                                    <p>(五)假期已满，既未继续办理请假手续，又不到岗上班的；</p>
