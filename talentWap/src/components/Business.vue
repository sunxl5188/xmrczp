                                    <p>第三条
                                        被派遣人员的含义。本办法所称被派遣人员(含人才派遣、劳务派遣)是指与本公司建立劳动关系，签订了劳动合同，由本公司管理人事关系、员工档案、薪酬福利、缴纳社会保险费，并服从本公司派往指定用工单位从事相应工作的员工。公司实行人才(劳务)派遣后，用工单位与公司签订《人才(劳务)派遣协议》；公司与被派遣人员签订《劳动合同》；用工单位可与被派遣人员签订《上岗协议》，双方只有使用关系，没有聘用合同关系。</p>
